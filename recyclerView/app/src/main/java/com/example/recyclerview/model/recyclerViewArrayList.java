package com.example.recyclerview.model;

public class recyclerViewArrayList {

    private final Integer image;
    private final String name;

    public recyclerViewArrayList(Integer image, String name) {
        this.image = image;
        this.name = name;
    }

    public Integer getImage() {
        return image;
    }

    public String getName() {
        return name;
    }
}
